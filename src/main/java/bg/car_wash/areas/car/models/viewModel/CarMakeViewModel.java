package bg.car_wash.areas.car.models.viewModel;

public class CarMakeViewModel {

	private Long id;

	private String make;

	public CarMakeViewModel() {
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getMake() {
		return make;
	}

	public void setMake(String make) {
		this.make = make;
	}
}
